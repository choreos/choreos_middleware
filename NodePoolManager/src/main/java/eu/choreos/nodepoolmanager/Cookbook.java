package eu.choreos.nodepoolmanager;

public class Cookbook {

	private String name;
	private String recipe;
	
	
	public String getRecipe() {
		return recipe;
	}
	public void setRecipe(String recipe) {
		this.recipe = recipe;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	
	
}
