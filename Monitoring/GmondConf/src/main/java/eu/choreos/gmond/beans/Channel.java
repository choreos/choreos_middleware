package eu.choreos.gmond.beans;

public class Channel {
	
	String host;
	String port;
		
	public String getHost() {
		return host;
	}
	public void setHost(String host) {
		this.host = host;
	}
	public String getPort() {
		return port;
	}
	public void setPort(String port) {
		this.port = port;
	}

}
